// JavaScript program to sort strings items of an array in both alphabetically ascending 
// and descending order.

//function to sort StringItems//
function sortStringItems(){
    var resultarray = ['s','h','a','l','i','n','i'];
    //sort() function is to sort in ascending order//
    console.log(resultarray.sort());
    //reverse() function is to sort in desending order//
    console.log(resultarray.reverse());
}
sortStringItems();